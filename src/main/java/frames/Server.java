    private void access_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_access_buttonActionPerformed
        foundKey = true;
        if (status) {
            hashList = hg.listGenerator("lucas");
            status = false;
        }

        int currentMinute = Calendar.getInstance().get(Calendar.MINUTE);
        String sCurrentMinute = String.valueOf(currentMinute);

        if (hashList.get(0).equals(sCurrentMinute)) {
            for (int i = 1; i < hashList.size(); i++) {
                if (key_txt.getText().equals(hashList.get(i).substring(0, 4))) {
                    JOptionPane.showMessageDialog(this,
                    "Acesso Garantido - Senha Correta",
                    "Acesso Garantido", JOptionPane.INFORMATION_MESSAGE);

                    for (int j = hashList.size() - 1; j >= i; j--) {
                        hashList.remove(j);
                    }
                    System.out.println("Nova lista de hash: " + hashList.size());
                    foundKey = false;
                    break;
                }
            }

            if (foundKey) {
                JOptionPane.showMessageDialog(this,
                    "Acesso Negado - Senha Incorreta",
                    "Acesso Negado", JOptionPane.ERROR_MESSAGE);
            }
            
        } else {
            JOptionPane.showMessageDialog(this,
                    "Acesso Negado - Senha Inv√°lida",
                    "Acesso Negado", JOptionPane.ERROR_MESSAGE);
            status = true;
            hashList.clear();
        }
    }//GEN-LAST:event_access_buttonActionPerformed

